package com.ssw.int1;

public class StaticBlockInheritance {
	public static void main(String[] args) {
		C cc = new C();
	}
}

class A {
	static {
		System.out.println("A");
	}
}

class B extends A {
	static {
		System.out.println("B");
	}
}

class C extends B {
	static {
		System.out.println("C");
	}
}